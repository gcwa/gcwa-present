package ca.gc.collectionscanada.web.model;

public class HeaderProperties {
	String altTitle;
	String imageURL;
	String canadaURL;
	String serviceURL;
	String headerDept;
	String headerDeptURL;
	String search;
	String govtCanadaSymbol;
	
	public String getAltTitle() {
		return altTitle;
	}
	public void setAltTitle(String altTitle) {
		this.altTitle = altTitle;
	}
	public String getImageURL() {
		return imageURL;
	}
	public void setImageURL(String imageURL) {
		this.imageURL = imageURL;
	}
	public String getCanadaURL() {
		return canadaURL;
	}
	public void setCanadaURL(String canadaURL) {
		this.canadaURL = canadaURL;
	}
	public String getServiceURL() {
		return serviceURL;
	}
	public void setServiceURL(String serviceURL) {
		this.serviceURL = serviceURL;
	}
	public String getHeaderDept() {
		return headerDept;
	}
	public void setDept(String headerDept) {
		this.headerDept = headerDept;
	}
	public String getHeaderDeptURL() {
		return headerDeptURL;
	}
	public void setHeaderDeptURL(String headerDeptURL) {
		this.headerDeptURL = headerDeptURL;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public String getGovtCanadaSymbol() {
		return govtCanadaSymbol;
	}
	public void setGovtCanadaSymbol(String govtCanadaSymbol) {
		this.govtCanadaSymbol = govtCanadaSymbol;
	}	
}