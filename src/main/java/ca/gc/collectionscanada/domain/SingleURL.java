package ca.gc.collectionscanada.domain;

public class SingleURL {
	String urlId;
	String urlDesc;
	String themNme;
	String crawlJobNbr;
	String crawlJobNme;
	
	public String getUrlId() {
		return urlId;
	}
	public void setUrlId(String urlId) {
		this.urlId = urlId;
	}
	public String getUrlDesc() {
		return urlDesc;
	}
	public void setUrlDesc(String urlDesc) {
		this.urlDesc = urlDesc;
	}
	public String getThemNme() {
		return themNme;
	}
	public void setThemNme(String themNme) {
		this.themNme = themNme;
	}
	public String getCrawlJobNbr() {
		return crawlJobNbr;
	}
	public void setCrawlJobNbr(String crawlJobNbr) {
		this.crawlJobNbr = crawlJobNbr;
	}
	public String getCrawlJobNme() {
		return crawlJobNme;
	}
	public void setCrawlJobNme(String crawlJobNme) {
		this.crawlJobNme = crawlJobNme;
	}
	
}
